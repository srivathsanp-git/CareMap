// Get hospitals near a lat/lng within maxMiles
export function getHospitalsNear(lat, lng, maxMiles = 60) {
  function haversine(lat1, lon1, lat2, lon2) {
    const R = 3958.8
    const dLat = ((lat2 - lat1) * Math.PI) / 180
    const dLon = ((lon2 - lon1) * Math.PI) / 180
    const a =
      Math.sin(dLat / 2) ** 2 +
      Math.cos((lat1 * Math.PI) / 180) *
        Math.cos((lat2 * Math.PI) / 180) *
        Math.sin(dLon / 2) ** 2
    return R * 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a))
  }

  return iowaHospitals
    .map(h => ({ ...h, distance: Math.round(haversine(lat, lng, h.lat, h.lng)) }))
    .filter(h => h.distance <= maxMiles)
    .sort((a, b) => a.distance - b.distance)
    .slice(0, 8)
}
